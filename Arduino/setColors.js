function setColors(reading) {
	if(reading < 150) {
		document.body.style.color="black";
		document.body.style.background = "white";
	}
	else if(reading < 325) {
		document.body.style.color="black";
		document.body.style.background = "white";
	}
	else if(reading < 440) {
		document.body.style.color="black";
		document.body.style.background = "white";
	}
	else if(reading < 570) {
		document.body.style.color="black";
		document.body.style.background = "white";
	}
	else {
		document.body.style.color="black";
		document.body.style.background = "white";
	}
}